#include<stdio.h>

int main0401()
{
	//现在我想让11改成15
	int a = 11;
	//00000000 00000000 00000000 00001011 - a的二进制
	//00000000 00000000 00000000 00000100 - 这里让11|4就可以得到（但这样太直接了，如果是前面更高位的数就不好算了）
	a = a | (1 << 2);
	//1<<2 - 这样更简单
	//00000000 00000000 00000000 00000001 - 00000000 00000000 00000000 00000100
	//00000000 00000000 00000000 00001111 - 结果
	printf("%d\n", a);


	//现在又想把15改成11
	//00000000 00000000 00000000 00001111 - 15
	//11111111 11111111 11111111 11111011 - 这里让15&11(这样也很直接)
	a = a & (~(1 << 2));
	//1<<2 - 先将1向左移动2位
	//00000000 00000000 00000000 00000100
	//然后将1<<2按位取反即可 ~(1<<2)
	//11111111 11111111 11111111 11111011
	//00000000 00000000 00000000 00001011 - 11
	printf("%d\n", a);
	return 0;
}