	/*
	!	非；非真为假，非假为真
	-	负
	+	正
	&	取地址
	sizeof	
	~	对一个数的二进制按位取反
	++	前自增；后自增
	--	前自减；后自减
	*	间接访问操作符
	*/

	//!
	int a = 10;
	//int a=0;
	printf("%d\n", a);
	printf("%d\n", !a);

	//sizeof计算的是变量/类型所占空间的大小，单位是字节
	//有三种写法
	printf("%d\n", sizeof(int));
	printf("%d\n", sizeof(a));
