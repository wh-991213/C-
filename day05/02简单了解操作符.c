#include<stdio.h>

int main0201(void)
{
	//算术操作符 +  -  *  /  %(取模)
	//同样是除那%和/的区别？
	//如果是除号，得到的结果是商
	int b = 5 / 2;
	printf("%d\n", b);//2

	//如果是取模，得到的是余数
	int a=5 % 2;
	printf("%d\n", a);//1
	return 0;
}

int main0202(void)
{
	//移位操作符 >>(右移)	 <<(左移)
	//移的是二进制位
	int a = 1;
	//00000000 00000000 00000000 00000001
	//就是把a的二进制序列整体向左移动一位，则左边的第1个0丢弃，右边补1个0
	//00000000 00000000 00000000 00000010
	int b=a << 1;
	
	//二进制10转换十进制2
	printf("%d\n", b);//2

	//当对a进行移动操作的时候，a本身是不变的；相当于b=a+1
	printf("%d\n", a);//1

	return 0;
}

int main0203(void)
{
	//(二进制)位操作符 &  ^  |

	//&按位  与			 同真为真，其余为假
	//|按位  或		     同假为假，其余为真
	//^按位	 异或
	
	int a = 3;
	int b = 5;
	int c=a&b;
	//在C语言中0为假，1为真；所有非0的值都是真值

	//3的二进制位011
	//5的二进制位101
	//			 001
	//	   十进制为1
	printf("%d\n", c);//1


	int d = 3;
	int e = 5;
	int f = d | e;
	//3的二进制位011
	//5的二进制位101
	//           111
	//     十进制为7
	printf("%d\n", f);//7


	int g = 3;
	int h = 5;
	int	i = g ^ h;//注意这里^不是次方，而是异或
	//异或的计算规律：对应的二进制位相同，则为0；对应的二进制位相异，则为1
	//3的二进制位011
	//5的二进制位101
	//           110
	//	   十进制为6
	printf("%d\n", i);//6
	return 0;
}

int main0204(void)
{
	//赋值操作符：=   +=   -=   *=   /=   &=   ^=   |=   >>=   <<=
	int a = 10;
	a = 20;//=赋值    ==判断相等

	//以下两种写法等价
	a = a + 10;
	a += 10;//这种叫做复合赋值符

	a=a&2;
	a &= 2;

	a = a >>= 2;
	a >>= 2;

	return 0;
}

