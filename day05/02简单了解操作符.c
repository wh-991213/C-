	//11111111 11111111 11111111 11111111(补码)
	//11111111 11111111 11111111 11111110(补码-1得到原码）
	//10000000 00000000 00000000 00000001(得到原码)
	//第1位是负，最后一位是1，所以是-1
	printf("%d\n", c);//-1(打印的是这个数的原码)


	//--    ++
	int d = 10;
	//int e = d++;//后自增；这里是先把d的值10赋值给e，然后d在自增(先使用，再++)
