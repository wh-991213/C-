#include<stdio.h>
//对于char类型的取值范围

//1.signed char
int main0301()
{
	//如果一个char类型要存放8位的二进制序列，以下是所有可能存放的格式 - 也就是2^8(256)次种格式
	// 内存补码
	// --------
	//|00000000| 0
	//|00000001| 1
	//|00000010| 2
	//|00000011| 3
	//|... ... |
	//|01111111| 127
	//|10000000| 特殊                   -128   对于-128：原110000000 反101111111 补110000000 - -128的原码补码是相同的，所以就把这个二进制序列直接规定成 -128
	//|10000001| 反10000000 原11111111  -127
	//|... ... |
	//|11111101| 反11111100 原10000011  -3
	//|11111110| 反11111101 原10000010  -2
	//|11111111| 反11111110 原10000001  -1
	// --------
	//所以对于signed char类型的区间是 -128 -> 127
	return 0;
}

//2.unsigned char 
int main0302()
{
	// 内存补码
	// --------
	//|00000000| 0
	//|00000001| 1
	//|00000010| 2
	//|00000011| 3
	//|... ... |
	//|01111111| 127
	//|10000000| 128
	//|10000001| 129
	//|... ... |
	//|11111101| 253
	//|11111110| 254
	//|11111111| 255
	// --------
	//所以对于unsigned char类型的区间是 0 -> 255
	return 0;
}
